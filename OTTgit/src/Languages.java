public class Languages {
    int Tid;
    String defLanguage;

    public void setTid(int tid) {
        this.Tid = tid;
    }
    public void setDefLanguage(String ttl) {
        this.defLanguage = ttl;
    }

    public int getTid() {
        return this.Tid;
    }
    public String getDefLanguage() {
        return this.defLanguage;
    }
}
